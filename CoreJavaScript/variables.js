// Variable types in JavaScript
// 1. var - function-scoped variable - Define a variable using var
var x = 10;
console.log(x); // Output: 10
// 2. let - block-scoped variable - Define a variable using let
let y = 20;
console.log(y);

// 3. const - block-scoped constant variable
const z = 30;
console.log(z);

var x = 40;
console.log(x);
